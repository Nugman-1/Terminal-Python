def remove_label(index):
    try:
        arrow_labels[index].grid_forget()
        del arrow_labels[index]
    except:
        pass

def display_stratagem_direction(set_num):
    global arrow_labels
    
    # Remove arrows
    for label in arrow_labels:
        label.grid_forget()
    
    arrow_labels = []
    counter = 0
    for x in direction_array:
        arrow_label = None
        if x == "up":
            arrow_label = Label(root, image=up_arrow_image)
        elif x == "down":
            arrow_label = Label(root, image=down_arrow_image)
        elif x == "left":
            arrow_label = Label(root, image=left_arrow_image)
        elif x == "right":
            arrow_label = Label(root, image=right_arrow_image)
            
        arrow_label.grid(row=0, column=counter, padx=20, pady=20)
        arrow_labels.append(arrow_label)
        counter += 1
    
    for i, label in enumerate(arrow_labels):
        root.after(speed * (i + 1) + set_num * 12000, remove_label, i)
